        if (Translations.TryGetValue("en", out var en) && en.TryGetValue(key, out var fallback))
            return fallback;
        return key;
    }

    public void SetLanguage(string code, Data.DatabaseContext? db = null)
    {
        if (!Translations.ContainsKey(code)) return;
        _currentLanguage = code;
        db?.SetSetting("Language", code);
        LanguageChanged?.Invoke();
    }

    public void LoadSaved(Data.DatabaseContext db)
    {
        var saved = db.GetSetting("Language");
        if (!string.IsNullOrEmpty(saved) && Translations.ContainsKey(saved))
            _currentLanguage = saved;
    }

    private static readonly Dictionary<string, string> CategoryKeyMap = new(StringComparer.OrdinalIgnoreCase)
    {
        ["Food"]          = "Cat.Food",
        ["Housing"]       = "Cat.Housing",
        ["Transport"]     = "Cat.Transport",
        ["Health"]        = "Cat.Health",
        ["Entertainment"] = "Cat.Entertainment",
        ["Clothing"]      = "Cat.Clothing",
        ["Education"]     = "Cat.Education",
        ["Travel"]        = "Cat.Travel",
        ["Bills"]         = "Cat.Bills",
        ["Other"]         = "Cat.Other",
    };

    public string TranslateCategory(string dbName)
    {
        if (string.IsNullOrEmpty(dbName)) return dbName;
        var spaceIdx = dbName.IndexOf(' ');
        if (spaceIdx < 0) return dbName;
        string emoji = dbName[..spaceIdx];
        string word  = dbName[(spaceIdx + 1)..].Trim();
        if (CategoryKeyMap.TryGetValue(word, out var key))
            return $"{emoji} {Get(key)}";
        return dbName;
    }
}

